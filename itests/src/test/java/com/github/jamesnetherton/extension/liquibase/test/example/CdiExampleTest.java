import java.io.File;
import org.jboss.arquillian.container.test.api.Deployment;
import org.jboss.arquillian.junit.Arquillian;
import org.jboss.shrinkwrap.api.Archive;
import org.jboss.shrinkwrap.api.ShrinkWrap;
import org.jboss.shrinkwrap.api.spec.JavaArchive;
import org.junit.Test;
import org.junit.runner.RunWith;

@RunWith(Arquillian.class)
public class CdiExampleTest extends LiquibaseTestSupport {

    @ChangeLogDefinition(fileName = "db-changes.xml")
    private String tableName;

    @Deployment
    public static Archive<?> deployment() {
        JavaArchive archive = ShrinkWrap.createFromZipFile(JavaArchive.class, new File("target/examples/wildfly-liquibase-example-cdi.jar"));
        archive.delete("db-changes.xml");
        return archive;
    }

    @Test
    public void testCdiExample() throws Exception {
        assertTableModified(tableName);
    }
}
